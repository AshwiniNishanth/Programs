package PackageOne;

public class SwapM1 {

	public static void main(String[] args) {
		int a=10;
		int b=50;
		System.out.println("Before swap number a is "+a+" and   b   is" +b);
		int t=a;
		a=b;
		b=t;
		System.out.println("After swap number a is "+a+" and    b   is" +b);
		

	}

}
